from fastapi import APIRouter, Depends, HTTPException, status
from pydantic import BaseModel
from datetime import datetime, timezone
from typing import List
from app.auth.dependencies import get_current_user
from app.database.connection import get_db
from app.ai.gemini_service import GeminiService
from app.utils.mongo import serialize_doc, serialize_list, to_object_id

router = APIRouter(prefix="/api/roadmaps", tags=["AI Learning Roadmaps"])

class RoadmapRequest(BaseModel):
    goal: str
    weeks: int = 8

@router.post("/generate")
async def generate_roadmap(req: RoadmapRequest, current_user: dict = Depends(get_current_user)):
    """Generate a custom interactive learning roadmap using Gemini AI."""
    db = get_db()
    
    # Generate roadmap weeks using Gemini Service
    weeks_list = await GeminiService.generate_roadmap(req.goal, req.weeks)
    
    new_roadmap = {
        "user_id": to_object_id(current_user["id"]) if db is not None else "dev_user_id",
        "goal": req.goal,
        "weeks": weeks_list,
        "created_at": datetime.now(timezone.utc)
    }
    
    if db is not None:
        result = await db.roadmaps.insert_one(new_roadmap)
        new_roadmap["_id"] = result.inserted_id
        
    return serialize_doc(new_roadmap)

