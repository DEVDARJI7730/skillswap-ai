@router.put("/{roadmap_id}/toggle/{week_number}")
async def toggle_week(roadmap_id: str, week_number: int, current_user: dict = Depends(get_current_user)):
    """Toggle the completion status of a specific week in the roadmap."""
    db = get_db()
    if db is None:
        return {"status": "success", "message": f"Toggled week {week_number} in development offline mode."}

    roadmap_obj_id = to_object_id(roadmap_id)
    roadmap = await db.roadmaps.find_one({"_id": roadmap_obj_id})
    if not roadmap:
        raise HTTPException(status_code=404, detail="Roadmap not found")

    weeks = roadmap.get("weeks", [])
    updated = False
    for week in weeks:
        if week.get("week_number") == week_number:
            week["completed"] = not week.get("completed", False)
            updated = True
            break
            
    if not updated:
        raise HTTPException(status_code=400, detail="Week number not found in roadmap")
        
    await db.roadmaps.update_one(
        {"_id": roadmap_obj_id},
        {"$set": {"weeks": weeks}}
    )
    
    return {"status": "success", "weeks": weeks}
